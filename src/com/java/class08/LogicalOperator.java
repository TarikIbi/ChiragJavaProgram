// Logical operators

// 1. And - &&
// 2. OR - ||
// 3. NOT - !

// * Logical operators works only with Boolean data types
// * Logical operators will be used to combine multiple conditions

// ==> Table for And operator (&&)

//  |value1 && value2 = Result
//  |true && true =  true
//  |true && false = false
//  |false && true = false
//  |false && false = false
//  In AND operator, when both are true the result will be true.

// ==> Table for OR operator (||)

//  |value1 || value2 = Result
//  |true || true =  true
//  |true || false = true
//  |false || true = true
//  |false || false = false
//  In OR operator, when one is true and second is false, the result has to be true.


package com.java.class19;

public class PrintPatterSquare {
    public static void main(String[] args) {

        // Print 5 number of rows
        for(int i = 1; i<=5; i++){

            // Print 1 to 5 in same line
            for(int j = 1; j<=5; j++){
                System.out.print(j);
            }
            System.out.println(); // THIS ONE PRINTING ENTER
        }

    }
}

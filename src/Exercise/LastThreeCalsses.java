package Exercise;

public class LastThreeCalsses {
    public static void main(String[] args) {
        int a = 24, b = 10;

        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a - b);

        System.out.println(a % b);
        System.out.println(a / b);
        System.out.println(b / a);
        System.out.println(b % a);

        System.out.println(91 % 20);
        System.out.println(20 % 91);
        System.out.println(90 / 20);
        System.out.println(20 / 90);

        System.out.println(21.4 / 10);
        System.out.println(10 / 21);
    }

}
